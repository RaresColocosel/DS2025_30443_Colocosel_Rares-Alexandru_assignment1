package com.ems.device.entities;

import jakarta.persistence.*;

@Entity
@Table(name = "device")
public class Device {

  @Id
  @GeneratedValue(strategy = GenerationType.IDENTITY)
  private Long id;

  @Column(nullable = false, length = 100)
  private String name;

  @Column(length = 255)
  private String description;

  @Column(name = "max_consumption_w")
  private Double maxConsumptionW;

  public Device() {
  }

  public Long getId() {
    return id;
  }

  public void setId(Long id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getDescription() {
    return description;
  }

  public void setDescription(String description) {
    this.description = description;
  }

  public Double getMaxConsumptionW() {
    return maxConsumptionW;
  }

  public void setMaxConsumptionW(Double maxConsumptionW) {
    this.maxConsumptionW = maxConsumptionW;
  }
}
